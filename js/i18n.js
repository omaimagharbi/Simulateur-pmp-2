function t(key, vars) {
  const lang = getLang();
  let str = (STRINGS[lang] && STRINGS[lang][key]) || (STRINGS.fr[key]) || key;
  if (vars) {
    Object.keys(vars).forEach(k => { str = str.replace(`{${k}}`, vars[k]); });
  }
  return str;
}

// Applique les traductions à tous les éléments [data-i18n] / [data-i18n-html] / [data-i18n-ph] de la page.
function applyI18n(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.getAttribute('data-i18n')); });
  scope.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = t(el.getAttribute('data-i18n-html')); });
  scope.querySelectorAll('[data-i18n-ph]').forEach(el => { el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph'))); });
  scope.querySelectorAll('[data-i18n-title]').forEach(el => { document.title = t(el.getAttribute('data-i18n-title')); });
}
